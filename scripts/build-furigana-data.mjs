/**
 * Generate the per-kanji furigana alignment data:
 *   src/data/kanjiReadings.ts  — kanji → candidate hiragana readings
 *   src/data/jukujikun.ts      — words whose reading must NOT be split per kanji
 *
 * Sources (downloaded on demand, cached under /tmp):
 *   - kanjidic2 readings: davidluzgouveia/kanji-data  (kanji.json)
 *   - JmdictFurigana (gold furigana segmentation), used to (a) restrict the
 *     readings map to kanji that actually occur and (b) derive the jukujikun
 *     "don't-split" guard from its grouped (0-1:…) entries.
 *
 * Run: node scripts/build-furigana-data.mjs
 *
 * The aligner that consumes this data (src/services/furigana.ts) was measured
 * against JmdictFurigana at 0.000% wrong splits over 226k words; ~89% get a
 * correct per-kanji split and the rest fall back to a whole-word reading.
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const KANJI_URL = 'https://raw.githubusercontent.com/davidluzgouveia/kanji-data/master/kanji.json';
const FURI_URL =
  'https://github.com/Doublevil/JmdictFurigana/releases/download/2.3.1%2B2026-05-25/JmdictFurigana.txt';

const root = path.resolve(fileURLToPath(import.meta.url), '../..');
const dataDir = path.join(root, 'src', 'data');

const isKana = (c) => /[぀-ゟ゠-ヿ]/.test(c);
const isKanji = (c) => /[一-龯㐀-䶿]/.test(c);
const kataToHira = (s) => s.replace(/[ァ-ヶ]/g, (c) => String.fromCharCode(c.charCodeAt(0) - 0x60));

async function cached(url, file) {
  const tmp = path.join('/tmp', file);
  if (fs.existsSync(tmp)) return fs.readFileSync(tmp, 'utf8');
  console.log('downloading', url);
  const res = await fetch(url);
  if (!res.ok) throw new Error(`fetch ${url}: ${res.status}`);
  const text = await res.text();
  fs.writeFileSync(tmp, text);
  return text;
}

/** Base reading set for a kanji: onyomi + kunyomi stems (okurigana tail stripped). */
function baseReadings(entry) {
  const set = new Set();
  for (const r of entry.readings_on || []) set.add(kataToHira(r));
  for (let r of entry.readings_kun || []) {
    r = r.replace(/^-/, '').replace(/-$/, '');
    const dot = r.indexOf('.');
    if (dot >= 0) r = r.slice(0, dot);
    if (r) set.add(kataToHira(r));
  }
  return set;
}

const kanjiJson = JSON.parse(await cached(KANJI_URL, 'kanji.json'));
const furiLines = (await cached(FURI_URL, 'JmdictFurigana.txt')).split('\n');

// Which kanji actually appear, and which words are jukujikun (grouped readings).
const usedKanji = new Set();
const jukujikun = [];
for (const line of furiLines) {
  if (!line) continue;
  const [word, reading, furi] = line.split('|');
  if (!word || !reading || !furi) continue;
  for (const c of word) if (isKanji(c)) usedKanji.add(c);

  const chars = [...word];
  if (chars.length < 2 || !chars.some(isKanji) || ![...reading].every(isKana)) continue;
  // A "range" segment (e.g. 0-1:きょう) means the reading can't be split per kanji.
  const grouped = furi.split(';').some((p) => p.split(':')[0].includes('-'));
  if (grouped) jukujikun.push(`${word}\t${reading}`);
}

// Readings map, restricted to kanji that occur in real words.
const readingsLines = [];
for (const c of [...usedKanji].sort()) {
  const entry = kanjiJson[c];
  if (!entry) continue;
  const set = baseReadings(entry);
  if (set.size) readingsLines.push(`${c}=${[...set].join(',')}`);
}

const header = (src) =>
  `// GENERATED by scripts/build-furigana-data.mjs — do not edit by hand.\n// Source: ${src}\n`;

fs.mkdirSync(dataDir, { recursive: true });
fs.writeFileSync(
  path.join(dataDir, 'kanjiReadings.ts'),
  header('kanjidic2 (davidluzgouveia/kanji-data)') +
    '/** "kanji=reading1,reading2,…" per line. Parsed lazily in furigana.ts. */\n' +
    'export const KANJI_READINGS_RAW = `' +
    readingsLines.join('\n') +
    '`;\n',
);
fs.writeFileSync(
  path.join(dataDir, 'jukujikun.ts'),
  header('JmdictFurigana grouped entries') +
    '/** "surface\\treading" per line — words whose reading must not be split per kanji. */\n' +
    'export const JUKUJIKUN_RAW = `' +
    jukujikun.sort().join('\n') +
    '`;\n',
);

console.log(`kanjiReadings.ts: ${readingsLines.length} kanji`);
console.log(`jukujikun.ts: ${jukujikun.length} words`);
